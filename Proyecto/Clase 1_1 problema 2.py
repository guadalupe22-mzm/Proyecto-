num1 = int(input("Digite el primer número: "))
num2 = int(input("Digite el segundo número: "))

Suma = num1 + num2
Resta = num1 - num2
Multi = num1 * num2
Division = num1 / num2
Modulo = num1 % num2
Elevado = num1 ** num2

print("El resultado de la suma: ", Suma)
print("El resultado de la resta: ", Resta)
print("El resultado de la multiplicacion: ", Multi)
print("El resultado de la division: ", Division)
print("El resultado del modulo: ", Modulo)
print("El resultado de la potencia: ", Elevado)

